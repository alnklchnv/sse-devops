<div id="review-body" class="review-body clearfix">

<p>Samsung's Galaxy Note20 officially comes in Mystic Green, and Mystic Gray color options in addition to the Mystic Bronze hero paint job.  Well it appears that the company's home market is getting a bit of a special treatment and an exclusive-for-now Mystic Red <a href="samsung_galaxy_note20_5g-10325.php">Samsung Galaxy Note20 5G</a>.</p>

<img class="inline-image" width="727" height="454" src="https://fdn.gsmarena.com/imgroot/news/20/08/samsung-galaxy-note20-red/-727/gsmarena_005.jpg" alt="Samsung is selling Mystic Red Galaxy Note20 in South Korea">

<p>The vanilla version of the phone has a bright red back panel and a similarly glowing S Pen as is the norm these days.</p>

<p>There is also a custom wallpaper for this phone, which is exclusive to the local carrier KT, although we would expect it to make it to the global scene eventually.</p>

<img class="inline-image" width="727" height="454" src="https://fdn.gsmarena.com/imgroot/news/20/08/samsung-galaxy-note20-red/-727/gsmarena_004.jpg" alt="Samsung is selling Mystic Red Galaxy Note20 in South Korea">

<p>On the inside it is the very same Galaxy Note20 phone like any other - Exynos 990 chipset with 5G modem and 8 GB RAM to keep it company. There is only one memory version in South Korea - 256 GB, and there is no slot for any memory card.</p>

<p class="image-row clearfix">
<a onclick='javascript:ShowImg2("news/20/08/samsung-galaxy-note20-red/-1200x900m/gsmarena_002.jpg");return false' href="#"><img alt="Mystic Red Samsung Galaxy Note20 5G" width="315" height="503" src="https://fdn.gsmarena.com/imgroot/news/20/08/samsung-galaxy-note20-red/-315/gsmarena_002.jpg"></a>
<a onclick='javascript:ShowImg2("news/20/08/samsung-galaxy-note20-red/-1200x900m/gsmarena_003.jpg");return false' href="#"><img alt="Mystic Red Samsung Galaxy Note20 5G" width="315" height="503" src="https://fdn.gsmarena.com/imgroot/news/20/08/samsung-galaxy-note20-red/-315/gsmarena_003.jpg"></a>
<br><font><strong>Mystic Red Samsung Galaxy Note20 5G</strong></font></p>

<p>The KT listing points out several times this phone has a flat display - a feature that was last seen in the Galaxy Note series back in 2015 with the  <a href="samsung_galaxy_note5-7431.php">Galaxy Note5</a>. The price of the phone is KRW1,026,500, or about $866, although getting it on contract will take that down.</p>

