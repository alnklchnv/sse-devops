<div id="review-body" class="review-body clearfix">

<p>The big shifts in our trending chart continue with major announcements by Samsung and the long delayed Google mid-ranger storming the chart.</p>

<p>The Galaxy Note20 Ultra is certainly the more interesting member of the newly announced S Pen duo - so much so that its 5G and 4G versions occupied the first two spots. In contrast, the vanilla Note20 didn't even manage to crack the top 10.</p>

<p>The Pixel 4a may have underwhelming hardware, but its aggressive price, decent base storage and Google's ability to extract better image quality than the camera hardware suggests helped it secure third spot.</p>

<p>The OnePlus Nord finds itself down in forth, but it's still the highest placed member of last week's top 10 and one place ahead of the former leader Samsung Galaxy M31s.</p>

<p>A Redmi trio follows - the newly unveiled 9 Prime sandwiched between the Note 9 Pro in sixth and Note 9 in eight. </p>

<P>The Samsung Galaxy A51 is now ninth as the Redmi 9 completes this installment of the chart.</p>

<p>The influx of newly announced phones means we also have a lot going down - the Black Shark 3S, the Redmi Note K30 Ultra, the Oppo Reno4 and the Galaxy A71 couldn't make it this time around. We are not sure about the others, but  the K30 Ultra will almost certainly be back come its official announcement next week.</p>

