<div id="review-body" class="review-body clearfix">

<p>On Tuesday, in a blog post published by YouTube, Google solidifies its efforts to migrate Google Play Music users over to YouTube Music, which will eventually replace the former. Over the past few months, Google has been updating YouTube Music with <a href="youtube_music_gets_a_new_explore_tab_and_song_lyrics-news-43089.php">new features</a> and has added the <a href="google_play_music_youtube_music_data_transfer-news-43165.php">ability to migrate</a> music, tastes, and playlists from Google Play Music.</p>


<img class="inline-image" width="727" height="454" src="https://fdn.gsmarena.com/imgroot/news/20/08/youtube-music-will-replace-google-music-in-2021/-727/gsmarena_001.jpg" alt="Google Play music is shutting down in December, will be replaced by YouTube Music">


<p>YouTube Music will completely succeed Play Music by December 2020. Starting in September, South Africa and New Zealand won’t be able to stream music through the Google Play Music app, and the same change will apply to all other global markets in October. Subscriptions will automatically be cancelled as well.</p>


<img class="inline-image" width="727" height="408" src="https://fdn.gsmarena.com/imgroot/news/20/08/youtube-music-will-replace-google-music-in-2021/-727/gsmarena_000.jpg" alt="Google Play music is shutting down in December, will be replaced by YouTube Music">


<p>Playlists, libraries, and content won’t be erased just yet. This will be everyone’s last change to migrate content and preferences to YouTube Music until December. In 2021, Google Play Music is going away. Starting later this month, users won’t be able to purchase more tunes on the old app.</p>

<p>YouTube Music is adding new features, including a new Player page redesign, a new Explore tab, and some new integrations with existing Google services. Google Maps integration with Google Play Music is coming, as well as an update to the Android TV app, and Google Assistant will be able to recommend new music for you.</p>

