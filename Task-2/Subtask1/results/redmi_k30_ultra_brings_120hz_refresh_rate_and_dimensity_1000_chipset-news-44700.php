<div id="review-body" class="review-body clearfix">

<p>You wait 10 years for a Xiaomi Ultra phone and now two come at the same time. Alongside the <a href="xiaomi_mi_10_ultra-10361.php">Xiaomi Mi 10 Ultra</a> comes the <a href="xiaomi_redmi_k30_ultra-10349.php">Redmi K30 Ultra</a>, a MediaTek Dimensity 1000+ powered alternative to the <a href="gsmarena.com/xiaomi_redmi_k30_pro-10140.php">K30 Pro</a>. It also doubles the refresh rate of the Pro to 120Hz and brings faster 33W wired charging at a more affordable pricetag.</p>

<img class="inline-image" width="727" height="427" src="https://fdn.gsmarena.com/imgroot/news/20/08/redmi-k30-ultra-ofic/-727/gsmarena_002.jpg" alt="Redmi K30 Ultra brings 120Hz refresh rate and Dimensity 1000+ chipset">

<p>The rest of the spec sheet is identical to the K30 Pro meaning a 6.67-inch AMOLED display with a 20MP pop-up camera and quad rear camera with a 64MP primary shooter. It’s accompanied by a 13MP ultrawide snapper, 5MP macro sensor and a 2MP depth helper.</p>

<p>MediaTek’s flagship Dimensity 1000+ SoC is the big change here and it's paired with 6/8 GB RAM and 128/256 or 512GB storage. The battery comes in at 4,500 mAh and you get the required charger to achieve its maximum 33W rate in the box. </p>

<p>The software side is covered by MIUI 12 on top of Android 10. Other notable additions include a stereo speaker setup and linear vibration motor.</p>

<img class="inline-image" width="727" height="363" src="https://fdn.gsmarena.com/imgroot/news/20/08/redmi-k30-ultra-ofic/-727/gsmarena_001.jpg" alt="Redmi K30 Ultra brings 120Hz refresh rate and Dimensity 1000+ chipset">

<p>The Redmi K30 Ultra will retail for CNY 1,999 ($287) in its 6/128 GB base trim. There’s also an 8/128 GB version for CNY 2,199($316) while CNY 2,499 ($360) will get you the 8/256 GB model. If those aren’t enough you can also scoop up the 8/512GB version for CNY 2,699 ($388). Official sales in China start on August 14. International availability is yet to be  detailed.</p>

