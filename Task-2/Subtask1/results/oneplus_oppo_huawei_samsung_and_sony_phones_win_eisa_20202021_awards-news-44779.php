<div id="review-body" class="review-body clearfix">

<p>The EISA committee has examined the smartphones that launched this year and awarded the best among them – here are the winners.</p>

<p>The <a href="oneplus_8_pro-9919.php">OnePlus 8 Pro</a> got the Best Smartphone award. The panel of judges was impressed with how the handset blends design and cutting-edge features, highlighting the 120 Hz 1440p+ display, Warp Charge 30 Wireless and the IP68-rated body with a soft-touch finish.</p>

<p>The Best Advanced Smartphone award went to the <a href="oppo_find_x2_pro-9529.php">Oppo Find X2 Pro</a> – the vegan leather proved to be a winner. The EISA team calls this “OPPO’s best smartphone yet” and is also fond of the 120 Hz HDR display, roomy 512 GB storage and fast 65 W charging.</p>

<img class="inline-image" width="727" height="336" src="https://fdn.gsmarena.com/imgroot/news/20/08/eisa-awards-2020/-727/gsmarena_001.jpg" alt="OnePlus, Oppo, Huawei, Samsung and Sony phones win EISA 2020-2021 awards">

<p>The <a href="huawei_p40_pro-10152.php">Huawei P40 Pro</a> is the top pick in the Smartphone Camera category. The 50 MP main and 12 MP periscope cameras, plus the 40 MP ultra wide Cine cam deliver “stunning daily photos and night shots of unmatched quality without unwanted noise or aberrations”.</p>

<p>The original <a href="samsung_galaxy_z_flip-10054.php">Samsung Galaxy Z Flip</a> was chosen as the top foldable phone. The old-school cool of the clamshell form factor with the advanced technology of is screen are combined to form an “inventive, impressive and practical smartphone”.</p>

<img class="inline-image" width="727" height="335" src="https://fdn.gsmarena.com/imgroot/news/20/08/eisa-awards-2020/-727/gsmarena_002.jpg" alt="OnePlus, Oppo, Huawei, Samsung and Sony phones win EISA 2020-2021 awards">

<p>The <a href="sony_xperia_1_ii-10096.php">Sony Xperia 1 II</a> earned the Multimedia Smartphone title of 2020-2021. The EISA members say it is “tailor-made for photo, film and music enthusiasts”. The speedy 20 fps burst mode with advanced autofocus, plus the Photography Pro and Cinematography Pro features were praised.</p>

<p>Huawei also scored another award – the <a href="huawei_watch_gt_2-9888.php">Huawei Watch GT 2</a> was chosen as the Best Smartwatch this year. It’s stylish and packed to the brim with exercise and health tracking features. That plus the two-week battery life put the GT 2 ahead of the competition.</p>

<img class="inline-image" width="727" height="336" src="https://fdn.gsmarena.com/imgroot/news/20/08/eisa-awards-2020/-727/gsmarena_003.jpg" alt="OnePlus, Oppo, Huawei, Samsung and Sony phones win EISA 2020-2021 awards">

<p>LG got a consolation prize, winning the best mobile speaker award with the Xboom Go PL7. The other audio accessory awards went to Dali IO-6 (best headphones), Bowers & Wilkins PX5 (best on-ear headphones) and Sennheiser Momentum True Wireless 2 (best in-ear headphones).</p>

