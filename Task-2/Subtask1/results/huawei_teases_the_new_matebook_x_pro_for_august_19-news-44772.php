<p>Exactly <a href="huawei_to_introduce_a_notebook_with_floating_display_honor_focuses_on_the_budget-news-44686.php">one week ago</a> a renowned tipster revealed that Huawei will be announcing a new laptop on August 19 but a new MateBook X Pro wasn't on the suspect's list. Now Huawei confirms that the MateBook X Pro is indeed getting a major update.</p>

<video width="727" height="409" preload="metadata" controls>
  <source src="https://fdn.gsmarena.com/imgroot/news/20/08/matebook-x-pro-teaser/gsmarena_v001.mp4" type="video/mp4">
</video>

<p>The teaser video talks about how thin and light it is meaning that it's probably better than its predecessor in terms of portability. Previous rumors also suggest that the screen will have absurdly thin bezels almost as if it's a floating screen. And resolution would be bumped up to 3K.</p>

<p>We are just two days away from the Chinese announcement so we won't have to wait much longer for more details.</p>

