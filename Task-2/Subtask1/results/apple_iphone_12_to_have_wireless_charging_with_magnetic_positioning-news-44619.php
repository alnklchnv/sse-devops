<div id="review-body" class="review-body clearfix">

<p>The last few generations of iPhones have been supporting wireless charging, but the 2020 iPhone 12 lineup might enhance the experience..</p>

<p>Cupertino is planning to introduce the first phone with magnetic positioning - very strong magnets will move the phone automatically to the optimal position on the charging pad.</p>

<p class="image-row clearfix">
<a onclick='javascript:ShowImg2("news/20/08/apple-iphone-12-magnetic-positioning/-1200x900m/gsmarena_005.jpg");return false' href="#"><img alt="This is how the iPhone 12 magnets will be positioned" width="207" height="331" src="https://fdn.gsmarena.com/imgroot/news/20/08/apple-iphone-12-magnetic-positioning/-207/gsmarena_005.jpg"></a>
<a onclick='javascript:ShowImg2("news/20/08/apple-iphone-12-magnetic-positioning/-1200x900m/gsmarena_003.jpg");return false' href="#"><img alt="This is how the iPhone 12 magnets will be positioned" width="207" height="331" src="https://fdn.gsmarena.com/imgroot/news/20/08/apple-iphone-12-magnetic-positioning/-207/gsmarena_003.jpg"></a>
<a onclick='javascript:ShowImg2("news/20/08/apple-iphone-12-magnetic-positioning/-1200x900m/gsmarena_004.jpg");return false' href="#"><img alt="This is how the iPhone 12 magnets will be positioned" width="207" height="331" src="https://fdn.gsmarena.com/imgroot/news/20/08/apple-iphone-12-magnetic-positioning/-207/gsmarena_004.jpg"></a>
<br><font><strong>This is how the iPhone 12 magnets will be positioned</strong></font></p>

<p>According to some leaked molds and coils, the first generation with magnetic positioning will be the iPhone 12 family, expected to arrive next month. The magnetic molds appeared on Weibo, where they were shot alongside other components like the plate for the camera  setup and the volume and power buttons.</p>



<img class="inline-image" width="727" height="557" src="https://fdn.gsmarena.com/imgroot/news/20/08/apple-iphone-12-magnetic-positioning/-727/gsmarena_002.jpg" alt="Apple iPhone 12 to have wireless charging with magnetic positioning">

<p>And as an added bonus the new feature will probably require a compatible charger to work. Which suggests that Apple might finally be ready to release an official wireless charger after the fiasco that was the Air Power project.</p>

